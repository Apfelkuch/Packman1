package Worldmanager;

public class Vektor2D {
    private int x;
    private int y;

    public Vektor2D(int x, int y) {
        this.x =x;
        this.y = y;
    }

    // GETTER && SETTER
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }
}
